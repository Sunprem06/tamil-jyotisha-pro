const NALLA_NERAM_BY_DAY = [
  '10:30 - 11:30, 19:30 - 20:30',
  '7:30 - 9:00, 18:00 - 19:30',
  '6:00 - 7:30, 15:00 - 16:30',
  '9:00 - 10:30, 22:30 - 24:00',
  '10:30 - 12:00, 18:00 - 19:30',
  '7:30 - 9:00, 12:00 - 13:30',
  '9:00 - 10:30, 16:30 - 18:00',
];

const RAHU_KALAM_BY_DAY = [
  '16:30 - 18:00',
  '7:30 - 9:00',
  '15:00 - 16:30',
  '12:00 - 13:30',
  '13:30 - 15:00',
  '10:30 - 12:00',
  '9:00 - 10:30',
];

const YAMAGANDAM_BY_DAY = [
  '12:00 - 13:30',
  '10:30 - 12:00',
  '9:00 - 10:30',
  '7:30 - 9:00',
  '6:00 - 7:30',
  '15:00 - 16:30',
  '13:30 - 15:00',
];

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders });
  }

  try {
    const supabase = createClient(
      Deno.env.get('SUPABASE_URL')!,
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    );

    const today = new Date();
    const dateStr = today.toISOString().split('T')[0];
    const dayOfWeek = today.getDay();

    const rasiData: Record<string, string> = {};
    for (const rasi of RASI_NAMES) {
      const templates = RASI_TEMPLATES[rasi];
      const daySeed = today.getDate() + today.getMonth() * 31;
      rasiData[rasi] = templates[daySeed % templates.length];
    }

    const { error } = await supabase.from('daily_rasi_palan').upsert({
      palan_date: dateStr,
      ...rasiData,
      nalla_neram: NALLA_NERAM_BY_DAY[dayOfWeek],
      rahu_kalam: RAHU_KALAM_BY_DAY[dayOfWeek],
      yamagandam: YAMAGANDAM_BY_DAY[dayOfWeek],
    }, { onConflict: 'palan_date' });

    if (error) throw error;

    return new Response(
      JSON.stringify({ success: true, date: dateStr }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (err) {
    return new Response(
      JSON.stringify({ error: (err as Error).message }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
